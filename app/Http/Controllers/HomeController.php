     */
    public function index()
    {
        $posts = Siswa::latest()->paginate(5);
    
        $bayar = Pembayaran::select('id_siswa', DB::raw('SUM(jumblah_bayar) as jumblah_bayar'), DB::raw('MAX(tgl_bayar) as tgl_bayar'))
        ->groupBy('id_siswa')
        ->orderBy('id_siswa', 'asc')
        ->paginate(5);

        //render view with posts
        return view('home', compact('posts', 'bayar'));
